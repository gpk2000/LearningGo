package main

import "fmt"

func main() {
	fmt.Println("go" + "programming") // strings can be concatenated with + op
	fmt.Println("1+2=", 1+2)
	fmt.Println("2*4=", 2*4)
	fmt.Println("7/3=", 7/3) // by default integer division
	fmt.Println("7.0/3.0=", 7.0/3.0)
	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!true)
}
